        $.ajax({
            url: 'notification_mail.php',
            method:"POST",
            data: {
                notify: 1,
                firstname: firstname,
                lastname: lastname,
                email: email,
                phone: phone,
                trans_type: trans_type
            },
            dataType: "json",
            success: function (r) {
                if (r.success == true) {
                    $("#exampleModalCenter").modal("hide");

                }else{
                    errorAlert("Notification email not sent")
                }
